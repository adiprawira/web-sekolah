						<p><strong>PROGRAM KEAHLIAN AKUNTANSI</strong></p>



						<p>Akuntansi merupakan salah satu Kompetensi Keahlian yang dimiliki SMK Negeri 1 Simpang Pematang. Kompetensi Keahlian ini termasuk dalam Jurusan Bisnis dan Manajemen yang telah ada sejak berdirinya SMK Negeri 1 Simpang Pematang. <br></p>



						<p>Garis besar tentang jurusan /kompetensi keahlian Akuntansi yaitu kompetensi keahlian yang memberikan bekal pengetahuan, ketrampilan, dan sikap. Sikap yang dimaksud adalah jujur, rapi, dan kerjasama untuk bisa mengerjakan kompetensi keahlian akuntansi sehingga bekerja sesuai dengan prosedur dan mandiri.</p>



						<p><strong>Tujuan Umum</strong></p>



						<p>Menghasilkan tamatan yang memiliki ketrampilan dan nilai sikap yang
							terintegrasi dan kecakapan kerja dalam bidang Akuntansi dengan
							menerapkan kewirausahan serta mampu mengadaptasi perkembangan masyarakat
							yang sesuai dengan kemajuan ilmu dan teknologi serta dapat memenuhi
							tuntutan dunia kerja masa sekarang dan masa yang akan datang.</p>



						<p><strong>Tujuan Khusus</strong></p>



						<p>Sekolah Menengah Kejuruan Akuntansi sebagai bagian dari Pendidikan Menengah bertujuan menyiapakan tamatan yang dapat:</p>



						<p>1. Mengembangkan sikap profesional dalam keahlian bisnis dan manajemen khususnya bidang Akuntansi.<br>2, Mampu memilih karir dan berkompetisi dalam dunia kerja.<br>3. Menjadi tenaga kerja tingkat menengah untuk mengisi kebutuhan dunia usaha dan industri.<br>4. Menjadi warga negara yang produktif, adaptif, dan kreatif.</p>



						<p><strong>Kurikulum</strong></p>



						<p>Berdasarkan Kurikulum SMK Negeri 1 Simpang Pematang. (spektrum 2008) susunan program Mata Diklat pada Kompetensi Keahlian Akuntansi ada tiga kelompok, yaitu Normatif, Adaptif, dan Produktif.</p>



						<p>Mata Diklat Produktif terdiri atas:</p>



						<p>1. Dasar Kompetensi Kejuruan<br>2. Menerapkan prinsip profesional bekerja.<br>3. Melakukan komunikasi bisnis.<br>4. Menerapkan Keselamatan, Kesehatan Kerja dan Lingkungan Hidup.</p>



						<p><strong>Kompetensi Kejuruan</strong></p>



						<p>1. Mengelola dokumen transaksi.<br>2. Memproses dokumen dana kas kecil.<br>3. Memproses dokumen dana kas di bank.<br>4. Memproses entry jurnal.<br>5. Memproses buku besar. </p>



						<p>6. Menyususn dan menyajikan laporan keuangan.<br>7. Mengelola kartu piutang.<br>8. Mengelola kartu persediaan.<br>9. Menegelola kartu aktifa tetap.<br>10.Mengelola kartu hutang.<br>11. Menyiapakan surat pemberitahuan pajak.<br>12. Mengoperasikan paket program pengolah angka (Spread Sheet)<br>13. Menyajikan laporan pokok produk.<br>14. Mengoperasikan aplikasi komputer akuntansi (MYOB)</p>



						<p>Salah satu kompetensi keahlian yang harus dikuasai adalah mengoperasikan MYOB. Mind Your Owen Businnes adalah software Akuntansi yang berfungsi membantu bagian keuangan dalam menyusun laporan keuangan lengkap dengan pengendalian inventory, penjualan, pembelian, dan aset perusahaan. Fungsi MYOB yaitu membantu dalam menyusun laporan keuangan. Sehingga mempermudah dalam membuat laporan keuangan.<br></p>



						<p>Selain tiga kelompok mata diklat tersebut, siswa Kompetensi Keahlian Akuntansi juga diberikan muatan lokal guna mendukung skill atau kemampuan siswa ketika lulus. Muatan lokal yang diberikan adalah Hitung Dagang yang diberikan kepada siswa kelas XI dan XII.</p>



						<p>Agar lebih menguasai ilmu yang telah diperoleh dari kegiatan pembelajaran, Siswa Jurusan Akuntansi menerapkan ilmunya tersebut melalui praktik di lingkungan SMK Negeri 1 Simpang Pematang. Praktik yang dilakukan yaitu piket di Bank Mini. Selain itu juga praktek di luar (Praktik Kerja Industri) selama 3 bulan yang wajib untuk siswa kelas XI.</p>



						<p><strong>Prospek Lulusan</strong></p>



						<p>Mengingat kemampuan dari lulusan Kompetensi
							Keahlian Akuntansi yang mempunyai kemampuan umum (sebagai manusia yang
							beriman dan bertaqwa kepada Tuhan Yang Maha Esa, berbudi pekerti luhur,
							sehat jasmani dan rohani, berkepribadian yang mantap dan mandiri serta
							mempunyai tanggung jawab dalam kemasyarakatan dan kebangsaan) dan
							kemampuan produktif. Sehingga tamatan Akuntansi diharapkan dapat bekerja
							di berbagai bidang.</p>



						<p>Lulusan&nbsp; akuntansi dapat bekerja di <strong>Instansi</strong><strong>
								Pemerintah, BUMN/BUMS, Perbankan, Lembaga Sosial, dan Usaha
								Sendiri/Mandiri, juga ahli sebagai kasir , teller, operasi mesin hitung,
								juru penggajian, operator komputer, administrasi gudang.</strong></p>

