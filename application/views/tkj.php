							<p>Materi yang dipelajari oleh Siswa TKJ adalah materi tentang perakitan PC dan Laptop, jaringan LAN (Local Area Network), jaringan WAN (Wide Area Network), Server, Admin jaringan.</p>



							<p>Urutan materi produktif yang dipelajari siswa TKJ adalah:<strong><br>
