							<p>1. Trouble shooting PC dan laptop,<br>2. Menginstal sistem operasi dan sofware,<br>3. Merawat PC.</p>



							<p><strong>Siswa kelas XI mempelajari materi produktif:</strong></p>



							<p> 1. Instalasi jaringan lokal (LAN)<br> 2. Instalasi linux<br> 3. Pengkabelan<br> 4. Perhitungan IP Address<br> 5. Mikrotik Router</p>



							<p><strong>Siswa kelas XII mempelajari materi produktif:</strong></p>



							<p> 1. Server berbasis text<br> 2. Instalasi jaringan wireless<br> 3. Administrasi Server, yang meliputi :<br> – Router<br> – DNS server<br> – WEB server<br> – Mail server<br> – DHCP server<br> – FTP server<br> – Proxy server<br> – Samba Server<br> – NTP Server</p>



